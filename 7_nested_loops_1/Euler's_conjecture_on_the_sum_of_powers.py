# В 1769 году Леонард Эйлер сформулировал обобщенную версию Великой теоремы Ферма, предполагая, что по крайней мере
# n энных степеней необходимо для получения суммы, которая сама является энной степенью для n>2.
# Программа опровержения гипотезы Эйлера (продержавшейся до 1967 года) и нахождения четырех положительных целых чисел,
# сумма 5-х степеней которых равна 5-й степени другого положительного целого числа.
# Таким образом, находит пять натуральных чисел a,b,c,d,e, удовлетворяющих условию: a^5+b^5+c^5+d^5=e^5
# В ответе указывается сумма a+b+c+d+e
# Считайте, что числа a,b,c,d,e не превосходят 150

for a in range(1, 151):
    for b in range(a, 151):
        for c in range(b, 151):
            for d in range(c, 151):
                e_fifth = a**5 + b**5 + c**5 + d**5
                e = int(e_fifth ** (1/5))
                if e <= 150 and e**5 == e_fifth:  # Проверка, что e в пределах и подходит
                    print(a + b + c + d + e)
                    break

