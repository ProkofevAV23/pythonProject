# Программа, которая считывает одну строку, после чего выводит «YES» (без кавычек),
# если во введённой строке есть подстрока «суббота» или «воскресенье», или «NO» (без кавычек) в противном случае

t = input()
s = 'суббота'
y = 'воскресенье'

if s in t or y in t:
    print('YES')
else:
    print('NO')

