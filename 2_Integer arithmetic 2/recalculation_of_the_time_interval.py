# Программа для пересчёта величины временного интервала, заданного в минутах, в величину,
# выраженную в часах и минутах в следующем формате:
# <исходное кол-во минут> мин - это <полное кол-во часов> час <оставшееся кол-во минут> минут.

a = input()
n = int(a)

print(n, 'мин - это', n // 60, 'час', n % 60, 'минут.')
