# Программа, которая предсказывает размер популяции организмов с 1-го по n-й день (включительно).
# Программа должна выводить номер дня, а затем через пробел размер популяции в этот день.

m = float(input())
p = int(input())
n = int(input())

for i in range(n):
    print(i+1, m * (1 + p/100) ** i)




