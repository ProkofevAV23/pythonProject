# Программа, которая считывает целое число и выводит для него на экран следующее и предыдущее целые числа в следующем формате:
# Следующее за числом <текущее число> число: <следующее число>
# Для числа <текущее число> предыдущее число: <предыдущее число>

a = input()
b = int(a)

print('Следующее за числом', b, 'число:', (b + 1))
print('Для числа', b, 'предыдущее число:', (b - 1))

