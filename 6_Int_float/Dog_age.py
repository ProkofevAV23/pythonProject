# На вход программе подаётся число n – количество собачьих лет.
# Программа вычисляет возраст собаки в человеческих
# годах по следующему алгоритму: в течение первых двух лет собачий год равен 10.5 человеческим годам,
# после этого каждый год собаки равен 4 человеческим годам.

dog_age = int(input())

human_age = (4 * (dog_age - 2))

if dog_age == 1:
    print(10.5)
elif dog_age == 2:
    print(21)
else:
    print(21 + human_age)

