# Программа, которая принимает на вход действительное число x и вычисляет по нему значение |x'|+|x.|
# |x'| - пол числа (округление x до ближайшего целого в меньшую сторону)
# |x.| - потолок числа (округление x до ближайшего целого в большую сторону).

from math import ceil, floor

x = float(input())

a = ceil(x)
b = floor(x)
d = a+b

print(d)



