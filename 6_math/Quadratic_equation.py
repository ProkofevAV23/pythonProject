# Программа, которая находит площадь правильного многоугольника.
# Правильный многоугольник — выпуклый многоугольник, у которого равны все стороны и все углы между смежными сторонами

from math import tan, pow, pi

n = int(input())
a = float(input())

c = pow(a, 2)
d = (pi/n)
e = tan(d)

S = (n * c) / (4 * e)
print(S)



